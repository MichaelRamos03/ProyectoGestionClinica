    private void txtIDRecetaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIDRecetaKeyTyped
       
         this.txtIDReceta.setText(this.txtIDReceta.getText().toUpperCase());
        char c = evt.getKeyChar();
        
        
        if (!Character.isDigit(c) && c != '-') {
            evt.consume();
            this.txtIDReceta.setBackground(Color.pink);
            this.txtIDReceta.setBorder(BorderFactory.createLineBorder(Color.red));
            this.lbError.setText("---> ERROR SOLO NUMEROS Y GUION <---");
            
        } else {
            this.txtIDReceta.setBackground(Color.white);
            this.txtIDReceta.setText(this.txtIDReceta.getText().toUpperCase());
            this.txtIDReceta.setBorder(BorderFactory.createLineBorder(new Color(0,120,215),1));
            this.lbError.setText(" ");
        }
        
    }//GEN-LAST:event_txtIDRecetaKeyTyped

